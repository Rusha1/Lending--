            jQuery('#btn_submit2').click(function(){
                // собираем данные с формы
                var user_email    = $('#email2').val();
                var user_them   = $('#them2').val();
                var user_text = $('#text2').val();
                // отправляем данные
                jQuery.ajax({
                    url: "form.php", // куда отправляем
                    type: "post", // метод передачи
                    data: { // что отправляем
                        "email":    user_email,
                        "them":   user_them,
                        "text":   user_text
                    },
                    error:function(){$("#erconts2").html("Произошла ошибка!");}, 
                    /* если произойдет ошибка в элементе с id erconts выведится сообщение*/                 
                    beforeSend: function() {                     
                        $("#erconts2").html("Отправляем данные...");                   
                    },                 
                    success: function(result){                     
                        /* В случае удачной обработки и отправки выполнится следующий код*/                     
                        $('#erconts2').html(result);                     
